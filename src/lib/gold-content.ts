/* ═══════════════ 해시 기반 인덱스 선택 ═══════════════ */
function hash(str: string): number {
  let h = 0;
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) - h + str.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

function pick<T>(arr: T[], slug: string, count: number, offset = 0): T[] {
  const h = hash(slug + offset);
  const result: T[] = [];
  const used = new Set<number>();
  for (let i = 0; i < count && result.length < count; i++) {
    let idx = (h + i * 11 + offset * 17) % arr.length;
    while (used.has(idx)) idx = (idx + 1) % arr.length;
    used.add(idx);
    result.push(arr[idx]);
  }
  return result;
}

/* ═══════════════ 메인 콘텐츠 생성 ═══════════════ */
export function generateGoldContent(venue: Venue) {
  const tagline = taglines[venue.slug] || `${venue.district}에서 빛나는 밤의 한 페이지`;
  const catLabel = venue.cat_slug === 'club' ? '클럽' : venue.cat_slug === 'night' ? '나이트' : '라운지';

  // 업소별 고유 서사 (3단락 선택, 전부 다른 조합)
  const selectedNarratives = pick(narrativePool, venue.slug, 3, 0);
  let narrativeText = selectedNarratives.map(fn => fn(venue)).join('\n\n');
  // 이름 반복 제한: 최대 2회만 유지, 나머지는 "이곳"으로 대체
  let nameCount = 0;
  const escaped = venue.name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  narrativeText = narrativeText.replace(new RegExp(escaped, 'g'), () => {
    nameCount++;
    return nameCount <= 2 ? venue.name : '이곳';
  });

  // 업소별 고유 FAQ (5개 선택, 전부 다른 조합)
  const selectedFAQ = pick(faqPool, venue.slug, 5, 1);
  const faq = selectedFAQ.map(fn => fn(venue));

  // 업소별 고유 팁 (6개 선택)
  const selectedTips = pick(tipPool, venue.slug, 6, 2);
  const tips = selectedTips.map(fn => fn(venue));

  return {
    tagline,
    title: `${venue.name} — ${tagline} | 골드나잇`,
    description: `${venue.region} ${venue.district} ${catLabel}. ${tagline}. 운영정보·분위기·방문팁 ${YEAR} 가이드.`,
    narrative: narrativeText,
    faq,
    tips,
    year: YEAR,
  };
}

